class Product {
    constructor(id, type, name, price, screen, backCamera, frontCamera, battery, img,  memory, sale) {
        this.id = id;
        this.type = type;
        this.name = name;
        this.price = price;
        this.screen = screen;
        this.backCamera = backCamera;
        this.frontCamera = frontCamera;
        this.battery = battery;
        this.img = img;
        this.memory = memory;
        this.sale = sale;          
    };
}